import enum
from datetime import datetime
from uuid import UUID, uuid4

from sqlalchemy import (
    BigInteger,
    Column,
    DateTime,
    Enum,
    ForeignKey,
    Index,
    Integer,
    String,
    Table,
    Text,
    func,
)
from sqlalchemy.dialects.postgresql import JSONB, TSVECTOR
from sqlalchemy.orm import Mapped, mapped_column, relationship

from app.db import Base


class TicketStatus(str, enum.Enum):
    NEW = "new"
    OPEN = "open"
    PENDING = "pending"          # waiting on customer
    ON_HOLD = "on_hold"          # waiting on something else
    RESOLVED = "resolved"
    CLOSED = "closed"
    MERGED = "merged"


class TicketPriority(str, enum.Enum):
    LOW = "low"
    NORMAL = "normal"
    HIGH = "high"
    URGENT = "urgent"


class TicketChannel(str, enum.Enum):
    EMAIL = "email"
    WEB = "web"
    SLACK = "slack"
    API = "api"


ticket_tags = Table(
    "ticket_tags_assoc",
    Base.metadata,
    Column("ticket_id", ForeignKey("tickets.id", ondelete="CASCADE"), primary_key=True),
    Column("tag_id", ForeignKey("ticket_tags.id", ondelete="CASCADE"), primary_key=True),
)


class TicketTag(Base):
    __tablename__ = "ticket_tags"
    id: Mapped[UUID] = mapped_column(primary_key=True, default=uuid4)
    name: Mapped[str] = mapped_column(String(64), unique=True)


class Ticket(Base):
    __tablename__ = "tickets"
    __table_args__ = (
        Index("ix_tickets_status_priority", "status", "priority"),
        Index("ix_tickets_assignee_status", "assignee_id", "status"),
        Index("ix_tickets_customer_created", "customer_id", "created_at"),
        Index("ix_tickets_search", "search_vector", postgresql_using="gin"),
        Index("ix_tickets_created_at", "created_at"),
    )

    id: Mapped[UUID] = mapped_column(primary_key=True, default=uuid4)
    # Human-friendly sequential number (e.g. #12345). Generated by sequence.
    number: Mapped[int] = mapped_column(BigInteger, unique=True, index=True)
    subject: Mapped[str] = mapped_column(String(500))
    status: Mapped[TicketStatus] = mapped_column(Enum(TicketStatus), default=TicketStatus.NEW, index=True)
    priority: Mapped[TicketPriority] = mapped_column(
        Enum(TicketPriority), default=TicketPriority.NORMAL, index=True
    )
    channel: Mapped[TicketChannel] = mapped_column(Enum(TicketChannel), default=TicketChannel.EMAIL)

    customer_id: Mapped[UUID] = mapped_column(ForeignKey("customers.id", ondelete="RESTRICT"), index=True)
    assignee_id: Mapped[UUID | None] = mapped_column(ForeignKey("users.id", ondelete="SET NULL"))
    team: Mapped[str | None] = mapped_column(String(64), index=True)

    # SLA bookkeeping
    sla_policy_id: Mapped[UUID | None] = mapped_column(ForeignKey("sla_policies.id"))
    first_response_due_at: Mapped[datetime | None] = mapped_column(DateTime(timezone=True))
    first_response_at: Mapped[datetime | None] = mapped_column(DateTime(timezone=True))
    resolve_due_at: Mapped[datetime | None] = mapped_column(DateTime(timezone=True))
    resolved_at: Mapped[datetime | None] = mapped_column(DateTime(timezone=True))
    closed_at: Mapped[datetime | None] = mapped_column(DateTime(timezone=True))

    # Merging
    merged_into_id: Mapped[UUID | None] = mapped_column(ForeignKey("tickets.id"))

    # Email threading: store the original Message-ID & References so replies route back.
    external_thread_id: Mapped[str | None] = mapped_column(String(998), index=True)

    metadata_: Mapped[dict] = mapped_column("metadata", JSONB, default=dict)

    # Postgres-generated tsvector for FTS (built in migration trigger).
    search_vector: Mapped[str | None] = mapped_column(TSVECTOR)

    created_at: Mapped[datetime] = mapped_column(DateTime(timezone=True), server_default=func.now(), index=True)
    updated_at: Mapped[datetime] = mapped_column(
        DateTime(timezone=True), server_default=func.now(), onupdate=func.now()
    )

    # Relationships
    customer = relationship("Customer", back_populates="tickets")
    assignee = relationship("User", back_populates="assigned_tickets", foreign_keys=[assignee_id])
    messages = relationship("Message", back_populates="ticket", cascade="all, delete-orphan", order_by="Message.created_at")
    tags = relationship("TicketTag", secondary=ticket_tags)
    sla_targets = relationship("SLATarget", back_populates="ticket", cascade="all, delete-orphan")
    csat = relationship("CSATResponse", back_populates="ticket", uselist=False)

    @property
    def is_open(self) -> bool:
        return self.status in {TicketStatus.NEW, TicketStatus.OPEN, TicketStatus.PENDING, TicketStatus.ON_HOLD}

    def __repr__(self) -> str:
        return f"<Ticket #{self.number} status={self.status}>"
